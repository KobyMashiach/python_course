arr = []

#  Add item to the END of the list
arr.append(5)
print(arr)
arr.append(7)
print(arr)
arr.append(2)
print(arr)

# Add item to specific location in the list
arr.insert(2, 8)
print(arr)

# Remove from the END of list
print(arr.pop())
print(arr)

# Remove from a specific location in the list
print(arr.pop(1))
print(arr)
